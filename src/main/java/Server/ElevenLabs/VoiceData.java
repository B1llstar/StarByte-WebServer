package Server;

public class VoiceData {
    private String voice_id;
    private String name;

    // Getter for voice_id
    public String getVoiceId() {
        return voice_id;
    }

    // Setter for voice_id
    public void setVoiceId(String voice_id) {
        this.voice_id = voice_id;
    }

    // Getter for name
    public String getName() {
        return name;
    }

    // Setter for name
    public void setName(String name) {
        this.name = name;
    }
}
